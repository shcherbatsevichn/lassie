$keysarraysort = [
	"season_value" =>"1",
	"collection_value" =>"2",
	"color_new" =>"3",
	"gender_value" =>"4",
	"INPUT_SIZE_NUMERIC" =>"5",
	"availability" =>"6"
];

foreach($arResult["ITEMS"] as $value){
	$arRes[$keysarraysort[$value["CODE"]]] = $value;
}
ksort($arRes);
